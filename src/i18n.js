function translatePlainText(text) {
  const trimmed = text.trim()
  if (!trimmed) return text
  if (uiTextTranslations[trimmed]) return text.replace(trimmed, uiTextTranslations[trimmed])

  const selectedMatch = trimmed.match(/^Selected (\d+) of 4$/)
  if (selectedMatch) return text.replace(trimmed, `Seleccionado ${selectedMatch[1]} de 4`)

  const countOfFourMatch = trimmed.match(/^(\d+) of 4$/)
  if (countOfFourMatch) return text.replace(trimmed, `${countOfFourMatch[1]} de 4`)

  const showingMatch = trimmed.match(/^Showing (\d+) of (\d+) events$/)
  if (showingMatch) return text.replace(trimmed, `Mostrando ${showingMatch[1]} de ${showingMatch[2]} eventos`)

  const targetMatch = trimmed.match(/^(\d+) target events$/)
  if (targetMatch) return text.replace(trimmed, `${targetMatch[1]} eventos objetivo`)

  const hoursMatch = trimmed.match(/^(\d+)\+ hrs$/)
  if (hoursMatch) return text.replace(trimmed, `${hoursMatch[1]}+ horas`)

  const hoursWeekMatch = trimmed.match(/^(\d+)\+ hrs\/week$/)
  if (hoursWeekMatch) return text.replace(trimmed, `${hoursWeekMatch[1]}+ horas/semana`)

  const minutesMatch = trimmed.match(/^(\d+) min$/)
  if (minutesMatch) return text.replace(trimmed, `${minutesMatch[1]} min`)

  const levelMatch = trimmed.match(/^Level (\d+): (.+)$/)
  if (levelMatch) {
    const levelLabel = uiTextTranslations[levelMatch[2]] || levelMatch[2]
    return text.replace(trimmed, `Nivel ${levelMatch[1]}: ${levelLabel}`)
  }

  return text
}

function interpolate(text, values = {}) {
  return Object.entries(values).reduce(
    (current, [key, value]) => current.replaceAll(`{${key}}`, String(value)),
    text,
  )
}

export function t(language, key, fallback, values = {}) {
  const englishText = fallback || key
  if (language !== 'es') return interpolate(englishText, values)
  const translated = uiTextTranslations[key] || uiTextTranslations[englishText] || translatePlainText(englishText)
  return interpolate(translated, values)
}

function translateAttributes(root) {
  const attributes = ['placeholder', 'aria-label', 'title']
  root.querySelectorAll('*').forEach((element) => {
    attributes.forEach((attribute) => {
      const value = element.getAttribute(attribute)
      if (!value) return
      const translated = translatePlainText(value)
      if (translated !== value) element.setAttribute(attribute, translated)
    })
  })
}

export function readLanguage() {
  try {
    const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY)
    return saved === 'es' ? 'es' : 'en'
  } catch {
    return 'en'
  }
}

export function translateVisibleText(language, root = document.body) {
  if (language !== 'es' || !root) return
  translateAttributes(root)
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT)
  const textNodes = []
  while (walker.nextNode()) textNodes.push(walker.currentNode)
  textNodes.forEach((node) => {
    const nextText = translatePlainText(node.nodeValue || '')
    if (nextText !== node.nodeValue) node.nodeValue = nextText
  })
}
